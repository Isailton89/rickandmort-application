public class Character {
    private String nome;
    private String especie;
    private String status;
    private String[] episodios;
    private String resumo;

    public Character(String nome, String especie, String status, String[] episodios, String resumo) {
        this.nome = nome;
        this.especie = especie;
        this.status = status;
        this.episodios = episodios;
        this.resumo = resumo;
    }

}
